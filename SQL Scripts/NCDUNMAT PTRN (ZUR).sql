SELECT CHDRPFX, CHDRCOY, CHDRNUM, PTRNEFF, BATCTRCDE, DATIME    
 FROM ZURDTA.PTRNPF                                   
 WHERE PTRNEFF BETWEEN 20240912 AND 20240918
 AND BATCTRCDE = 'T405'
 AND CHDRNUM IN (SELECT FORENUM FROM ZURDTA.MBCHPF) 
 AND DATIME >= '2024-09-01'
  ORDER BY CHDRNUM
 ;
 
